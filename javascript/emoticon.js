function emoji(text) {
  var map = {
    ":)": '🙂',
    ':D' : '😁',
    ':p': "😛",
    ';p' : "😜",
    ';)' : "😉",
    ':(' : "🙁",
    ':|' : "😐",
    '-_-' : "😑",
    'B)' : "😎",
    '8)' : "🤓",
    ":o" : "😯",
    ":O" : "😮",
    "|-|" : "😴",
    '(:' : "🙃",
    'p)' : "🧐"

  };
 return text.replace(/:\)|:D|:p|;p|;\)|:\)|:\(|:\||-_-|B\)|8\)|:o|:O|\|-\||\(:|p\)/gi, function(m) { return map[m]; });
 
}